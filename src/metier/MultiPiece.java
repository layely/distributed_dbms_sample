package metier;

public class MultiPiece extends Logement{
	
	
	public MultiPiece(){
		
	}
	private int parking;
	private int nombreBalcon;
	private int nombreChambre;
	private int nombreToilette;
	private int nombreCuisine;
	public int getParking() {
		return parking;
	}
	public void setParking(int parking) {
		this.parking = parking;
	}
	public int getNombreBalcon() {
		return nombreBalcon;
	}
	public void setNombreBalcon(int nombreBalcon) {
		this.nombreBalcon = nombreBalcon;
	}
	public int getNombreChambre() {
		return nombreChambre;
	}
	public void setNombreChambre(int nombreChambre) {
		this.nombreChambre = nombreChambre;
	}
	public int getNombreToilette() {
		return nombreToilette;
	}
	public void setNombreToilette(int nombreToilette) {
		this.nombreToilette = nombreToilette;
	}
	public int getNombreCuisine() {
		return nombreCuisine;
	}
	public void setNombreCuisine(int nombreCuisine) {
		this.nombreCuisine = nombreCuisine;
	}
	

}
